#include <iostream>
#include <math.h>
using namespace std;
//---------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------

class Polinomio{

  private:

    int gr;
    double coef[100];

  public:

//-------------------------------------------------------------------------------------------------------------
//--------------------------------constructor-----------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------

    Polinomio (int grd, double coeficientes[]){
      (*this).gr = grd;
      while (grd >= 0){
        (*this).coef[grd] = coeficientes[grd];
        grd--;
      } // while()
    } // Polinomio

//-------------------------------------------------------------------------------------------------------------
//--------------------------------metodos----------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------

    int getGrado (){
      return (*this).gr;
    } //grado()

    double getCoeficienes (unsigned int pos){
      return (*this).coef[pos];
    } //getCoeficienes()

    double getValor (double val){
      double res = 0;
      while (gr >= 0){
        gr--;
        double s;
        s = ((pow(val, gr))*coef[gr]);
        res = res + s ;
      } //while()
      return res;
    } //getValor()

}; // class

//---------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------
int main () {
  double l1[100]={1.0,2.0,3.0};
  Polinomio p1 (3, l1);
  cout << p1.getGrado() << endl;
  cout << p1.getCoeficienes(0) << endl;
  cout << p1.getValor(2)<< endl;
} // main()
